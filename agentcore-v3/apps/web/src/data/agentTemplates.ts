export function extractFromPrompt(text: string): Partial<AgentTemplate> | null {
  const lower = text.toLowerCase();
  const scores: Record<string, number> = {};
  
  for (const [keyword, templateId] of Object.entries(KEYWORD_MAP)) {
    if (lower.includes(keyword)) {
      scores[templateId] = (scores[templateId] || 0) + 1;
    }
  }
  
  const bestMatch = Object.entries(scores).sort((a, b) => b[1] - a[1])[0];
  if (!bestMatch || bestMatch[1] < 2) return null;
  
  return AGENT_TEMPLATES.find(t => t.id === bestMatch[0]) || null;
}
